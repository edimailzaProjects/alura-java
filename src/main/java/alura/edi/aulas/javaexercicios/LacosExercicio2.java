package alura.edi.aulas.javaexercicios;

public class LacosExercicio2
{
    public static void main(String[] args)
    {
        // Lista de exercícios da USP

        System.out.println("--Tabuada--");
        System.out.println();

        for (int i = 0; i <= 10; i++)
        {
            System.out.println();
            for (int j = 0; j <= 10; j++)
            {
                System.out.println(i + " + " + j + " = " + (i + j));
            }
        }
    }

}
