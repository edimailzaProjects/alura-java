package alura.edi.aulas.java;

public class Inteiros
{
    public static void main(String[] args)
    {
        System.out.println("Testa variáveis");

        int idade = 35;

        System.out.println("Idade: " + idade);

        System.out.println("Em 2023: " + (idade + 2));

        int idade2 = idade + 2;

        System.out.println("Em 2023: " + idade2);

        System.out.println("Mal concatenado: em 2023: " + idade + 2);
    }

}
