package alura.edi.aulas.java;

public class CharStrings
{
    public static void main(String[] args)
    {
        System.out.println("STRINGS E CHARS");
        char umaLetra = 'a';
        System.out.println(umaLetra);

        char umValor = 65;
        System.out.println(umValor);
        System.out.println(umValor + 1);
        System.out.println((char) (umValor + 1));

        String palavra = "Humildade em Aprender ";
        System.out.println(palavra);

        System.out.println((palavra + 2022));
    }

}
